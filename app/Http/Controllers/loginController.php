     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $credentials = $request->only('email', 'password');
        if (Auth::attempt($credentials)) {
            $redirect = "singup-create";
            return response()->json(['success' => false, 'message' => 'Successfull login','redirect' => $redirect ]);
        }
        $redirect = "login-create";
        return response()->json(['success' => false, 'message' => 'Invalid credentials','redirect' => $redirect ]);
    }


